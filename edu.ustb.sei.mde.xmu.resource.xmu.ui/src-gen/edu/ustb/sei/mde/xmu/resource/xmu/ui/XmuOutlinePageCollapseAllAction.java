package edu.ustb.sei.mde.xmu.resource.xmu.ui;

public class XmuOutlinePageCollapseAllAction extends edu.ustb.sei.mde.xmu.resource.xmu.ui.AbstractXmuOutlinePageAction {
	
	public XmuOutlinePageCollapseAllAction(edu.ustb.sei.mde.xmu.resource.xmu.ui.XmuOutlinePageTreeViewer treeViewer) {
		super(treeViewer, "Collapse all", org.eclipse.jface.action.IAction.AS_PUSH_BUTTON);
		initialize("icons/collapse_all_icon.gif");
	}
	
	public void runInternal(boolean on) {
		if (on) {
			getTreeViewer().collapseAll();
		}
	}
	
	public boolean keepState() {
		return false;
	}
	
}
