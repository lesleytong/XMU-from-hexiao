package edu.ustb.sei.mde.xmu2.runtime.exceptions;

public class InvalidBackwardEnforcementException extends RuntimeException {

	public InvalidBackwardEnforcementException() {
		
	}

	public InvalidBackwardEnforcementException(String message) {
		super(message);
		
	}

	public InvalidBackwardEnforcementException(Throwable cause) {
		super(cause);
		
	}

	public InvalidBackwardEnforcementException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public InvalidBackwardEnforcementException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

}
