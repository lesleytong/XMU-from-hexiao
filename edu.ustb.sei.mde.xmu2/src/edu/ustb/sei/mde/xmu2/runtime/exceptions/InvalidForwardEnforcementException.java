package edu.ustb.sei.mde.xmu2.runtime.exceptions;

public class InvalidForwardEnforcementException extends FatalException {

	public InvalidForwardEnforcementException() {
		
	}

	public InvalidForwardEnforcementException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public InvalidForwardEnforcementException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public InvalidForwardEnforcementException(String message) {
		super(message);
		
	}

	public InvalidForwardEnforcementException(Throwable cause) {
		super(cause);
		
	}

}
