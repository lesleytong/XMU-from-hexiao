package edu.ustb.sei.mde.morel.resource.morel.ui;

public class MorelQuickAssistAssistant extends org.eclipse.jface.text.quickassist.QuickAssistAssistant implements org.eclipse.jface.text.quickassist.IQuickAssistAssistant {
	
	public MorelQuickAssistAssistant(edu.ustb.sei.mde.morel.resource.morel.IMorelResourceProvider resourceProvider, edu.ustb.sei.mde.morel.resource.morel.ui.IMorelAnnotationModelProvider annotationModelProvider) {
		setQuickAssistProcessor(new edu.ustb.sei.mde.morel.resource.morel.ui.MorelQuickAssistProcessor(resourceProvider, annotationModelProvider));
		setInformationControlCreator(new org.eclipse.jface.text.AbstractReusableInformationControlCreator() {
			public org.eclipse.jface.text.IInformationControl doCreateInformationControl(org.eclipse.swt.widgets.Shell parent) {
				return new org.eclipse.jface.text.DefaultInformationControl(parent, (org.eclipse.jface.text.DefaultInformationControl.IInformationPresenter) null);
			}
		});
	}
	
	public boolean canAssist(org.eclipse.jface.text.quickassist.IQuickAssistInvocationContext invocationContext) {
		return false;
	}
	
	public boolean canFix(org.eclipse.jface.text.source.Annotation annotation) {
		return true;
	}
	
}
